/**
 * Create an icon element
 * @param {string} name - Icon name from icons object
 * @param {number} size - Size in pixels
 * @param {string} className - Additional CSS classes
 * @returns {HTMLElement} - SVG icon element
 */
export function createIcon(name, size = 24, className = '') {
  const div = document.createElement('div');
  div.innerHTML = icons[name] || '';
  const svg = div.firstElementChild;
  if (svg) {
    svg.setAttribute('width', size);
    svg.setAttribute('height', size);
    if (className) svg.className = className;
  }
  return svg;
}

/**
 * Create a card element
 * @param {Object} options - Card options
 * @returns {HTMLElement} - Card element
 */
export function createCard({ className = '', onClick = null } = {}) {
  const card = document.createElement('div');
  card.className = `card ${className}`;
  if (onClick) {
    card.style.cursor = 'pointer';
    card.addEventListener('click', onClick);
  }
  return card;
}

/**
 * Create a button element
 * @param {Object} options - Button options
 * @returns {HTMLElement} - Button element
 */
export function createButton({ 
  text, 
  onClick, 
  variant = 'primary', 
  className = '', 
  icon = null,
  iconSize = 18
} = {}) {
  const button = document.createElement('button');
  button.className = `btn btn-${variant} ${className}`;
  button.addEventListener('click', onClick);
  
  if (icon) {
    const iconEl = createIcon(icon, iconSize);
    iconEl.classList.add('btn-icon');
    button.appendChild(iconEl);
  }
  
  if (text) {
    const textSpan = document.createElement('span');
    textSpan.textContent = text;
    button.appendChild(textSpan);
  }
  
  return button;
}
